    private void inid(){
        this.id -=1;
    }
    
    private void deid(){
        this.id -=1;
    }
    
    private String setKode(){
        this.inid();
        String SDF = new SimpleDateFormat("yyMMdd").format(new Date());
        this.kode = String.format(SDF+"%02d", this.id);
        return kode;
    }
    
    private Object [] addItem (String nama, int jumlah){
        float harga = 0;
        ComboTransaksi itemadd = new ComboTransaksi();
        for(int i = 0; i< itemadd.getNama().size(); i++){
            if(nama.equalsIgnoreCase(itemadd.getNama().get(i))){
                harga = itemadd.getHarga().get(i);
            }
        }
        Object [] o ={
            nama, harga, jumlah,
        };
        return o;
    }
    
    private void updatejumlah(String nama, int add){
        ArrayList<String> item = new ArrayList<>();
        for (int i = 0; i < TModel.getRowCount(); i++){
            item.add (TModel.getValueAt(i, 0).toString());
        }
        for (int i = 0; i < item.size(); i++){
            if(item.get(i).equals(nama)){
                int jumlah = new Integer (TModel.getValueAt(i, 2).toString());
                TModel.setValueAt(jumlah+add, i, 2);
            }
        }
    }
     
    private boolean isDuplicate(String nama){
        boolean result = false;
        ArrayList <String> item = new ArrayList<>();
        for(int i = 0; i < TModel.getRowCount(); i++){
            item.add (TModel.getValueAt(i, 0).toString());
        }
        for (String i : item){
            if (i.equals((nama))){
                result = true;
            }
        }
        return result;
    }
    
    private boolean isEmpty(){
        return this.Tabel.getModel().getRowCount() <= 0;
    }
    
    private void pesan(){
        if(isEmpty()){
            this.Simpan.setEnabled(false);
            this.Hapus.setEnabled(false);
        }else{
            this.Simpan.setEnabled(true);
            this.Hapus.setEnabled(true);
        }
    }
    
